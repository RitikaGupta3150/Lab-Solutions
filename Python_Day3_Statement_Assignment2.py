#Using input function take two number and then swap the number

#taking two numbers as input from users
a=int(input("Enter first number:"))
b=int(input("Enter second number:"))
#swapping the numbers
a,b=b,a
#Displaying the swapped numbers
print(f"After swapping, the first number is {a} and the second number is {b}")


