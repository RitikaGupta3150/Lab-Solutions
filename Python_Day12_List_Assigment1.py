#1. Write a Python program to sum all the items in a list.

numbers = [10, 20, 30, 40, 50]  # List of numbers
total_sum = 0  # Initialize sum

for num in numbers:  # Loop through each number in the list
    total_sum += num  # Add each number to the sum

print("The sum of all items in the list is:", total_sum)

